package chapter_2;

public class CharArithDemo {
    /**
     * Program entry point.
     * @param args command Line arguments
     */
    public static void main(String[] args) {
        char ch;
        ch = 'X';
        System.out.println("ch содержит = " + ch);
        ch++; // Переменную типа char можно инкрементировать
        System.out.println("ch содержит = " + ch);
        ch = 90; //присвоить переменной ch значение ' Z' Переменной типа char можно присвоить целочисленное значение
        System.out.println("ch содержит = " + ch);
    }
}
